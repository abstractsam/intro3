#use any number  between 1-5
#they are rewarded

bettingnumber =  int(input("Enter any number between 1-5"))
if  bettingnumber == 1:
    print(" You have won a herd of cows")
elif bettingnumber == 2:
    print(" You have won a Mercedes car")
elif bettingnumber == 3:
    print("You have won a mansion")
elif bettingnumber == 4:
    print("You have won a house")
elif bettingnumber == 5:
    print("You have won 100,000 dollars")
else:
    print("Invalid Number entered")










